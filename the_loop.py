def intro():
    """Displays welcome message and asks user
    if they would like to play the game."""

    print(welcome)

    ask_to_play = 0

    while ask_to_play != 1 and ask_to_play != 2:
        try:
            ask_to_play = int(input(
                "Would you like to continue? "
                "Enter 1 for yes or 2 for no."
            ))
        except ValueError:
            print("⚠️ Your entry must be 1 or 2. Please try again.")

    if ask_to_play == 1:
        print(user_chooses_to_play)
        return True
    else:
        print("Well that was anticlimactic. Maybe Next Time")
        return False

